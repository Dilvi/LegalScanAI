        # –î–æ–±–∞–≤–ª—è–µ–º –≤ –∏—Å—Ç–æ—Ä–∏—é
        self.add_message("assistant", cleaned)

        # –§–æ—Ä–º–∞—Ç–∏—Ä—É–µ–º Markdown ‚Üí HTML
        return self.formatter.format_text(cleaned)
